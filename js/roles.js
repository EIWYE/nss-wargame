/* ===== 6. 辅助函数 ===== */
const RoleSystem = {
  /* 获取当前用户角色配置 */
  getProfile(){
    const session = (typeof Auth !== 'undefined') ? Auth.getSession() : null;
    const roleId = session ? session.role : 'trainee';
    const roleCfg = (typeof Auth !== 'undefined') ? (Auth.ROLES[roleId] || {}) : {};
    return {
      roleId,
      roleName: roleCfg.name || '见习学员',
      roleColor: roleCfg.color || '#7a8aa8',
      clearance: session ? session.clearance : 'internal',
    };
  },

  /* 获取角色可见的功能区列表（按优先级排序） */
  getVisibleZones(){
    const p = this.getProfile();
    const zones = ROLE_ZONES[p.roleId] || ROLE_ZONES.trainee;
    const list = zones.sortOrder || [];
    const primary = zones.primary || [];
    const restricted = zones.restricted || [];
    return list.map(id => ({
      id,
      cfg: ZONE_CONFIG[id] || null,
      isPrimary: primary.includes(id),
      isRestricted: restricted.includes(id),
    }));
  },

  /* 获取角色专属推演模块 */
  getModules(){
    const p = this.getProfile();
    return ROLE_MODULES[p.roleId] || ROLE_MODULES.trainee || [];
  },

  /* 获取角色仪表盘配置 */
  getDashboard(){
    const p = this.getProfile();
    return ROLE_DASHBOARD[p.roleId] || ROLE_DASHBOARD.trainee;
  },

  /* 获取角色场景分类过滤 */
  getScenarioCategories(){
    const p = this.getProfile();
    return ROLE_SCENARIO_CATEGORIES[p.roleId] || ROLE_SCENARIO_CATEGORIES.trainee;
  },

  /* 获取角色引导内容 */
  getOnboarding(){
    const p = this.getProfile();
    return ROLE_ONBOARDING[p.roleId] || ROLE_ONBOARDING.trainee;
  },

  /* 检查功能区是否对当前角色可见 */
  isZoneVisible(zoneId){
    const p = this.getProfile();
    const zones = ROLE_ZONES[p.roleId] || ROLE_ZONES.trainee;
    return !(zones.restricted || []).includes(zoneId);
  },

  /* 检查功能区是否为角色主要功能区 */
  isZonePrimary(zoneId){
    const p = this.getProfile();
    const zones = ROLE_ZONES[p.roleId] || ROLE_ZONES.trainee;
    return (zones.primary || []).includes(zoneId);
  },
};

